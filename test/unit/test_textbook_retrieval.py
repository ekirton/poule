def _write_html(tmp_path, volume, filename, content):
    """Write an HTML file into a volume directory."""
    vol_dir = tmp_path / volume
    vol_dir.mkdir(parents=True, exist_ok=True)
    path = vol_dir / filename
    path.write_text(content)
    return path


