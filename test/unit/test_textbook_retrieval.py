class TestEducationStorageEmbeddings:
    """EducationStorage write/load embedding round-trip."""

    def test_write_and_load_embeddings(self, tmp_path):
        db_path = tmp_path / "education.db"
        EducationStorage.create(db_path)
        chunks = [_make_chunk("text one"), _make_chunk("text two")]
        ids = EducationStorage.write_chunks(db_path, chunks)
        vectors = [_random_unit_vector(384, seed=i) for i in range(2)]
        EducationStorage.write_embeddings(db_path, ids, vectors)
        matrix, id_map = EducationStorage.load_embeddings(db_path)
        assert matrix.shape == (2, 384)
        assert id_map.shape == (2,)
        assert set(id_map.tolist()) == set(ids)
        # Verify vectors are L2-normalized after round-trip
        for row in range(matrix.shape[0]):
            norm = np.linalg.norm(matrix[row])
            assert abs(norm - 1.0) < 1e-4


