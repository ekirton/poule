def _make_chunk(
    text="Sample text",
    volume="lf",
    chapter="Basics",
    section="Introduction",
    anchor_id="lab10",
):
    return Chunk(
        text=text,
        code_blocks=["Definition x := 1."],
        metadata=ChunkMetadata(
            volume=volume,
            volume_title="Logical Foundations",
            chapter=chapter,
            chapter_file="Basics.html",
            section_title=section,
            section_path=["Introduction"],
            anchor_id=anchor_id,
        ),
        token_count=len(text.split()),
    )


