class TestEducationEncoderEncode:
    """EducationEncoder.encode maps text to L2-normalized 384-dim vectors."""

    @pytest.fixture
    def mock_encoder(self):
        """Create a mock encoder that returns predictable vectors."""
        enc = Mock(spec=EducationEncoder)
        enc.encode.side_effect = lambda text: _random_unit_vector(
            384, seed=hash(text) % 2**31
        )
        enc.encode_batch.side_effect = lambda texts: [
            _random_unit_vector(384, seed=hash(t) % 2**31) for t in texts
        ]
        enc.model_hash.return_value = "abc123"
        return enc

    def test_encode_returns_384_dim(self, mock_encoder):
        vec = mock_encoder.encode("induction on natural numbers")
        assert vec.shape == (384,)

    def test_encode_l2_normalized(self, mock_encoder):
        vec = mock_encoder.encode("induction on natural numbers")
        norm = np.linalg.norm(vec)
        assert abs(norm - 1.0) < 1e-5

    def test_encode_deterministic(self, mock_encoder):
        v1 = mock_encoder.encode("same text")
        v2 = mock_encoder.encode("same text")
        np.testing.assert_array_equal(v1, v2)

    def test_encode_batch_preserves_order(self, mock_encoder):
        texts = ["alpha", "beta", "gamma"]
        batch = mock_encoder.encode_batch(texts)
        individual = [mock_encoder.encode(t) for t in texts]
        assert len(batch) == 3
        for b, i in zip(batch, individual):
            np.testing.assert_array_equal(b, i)


